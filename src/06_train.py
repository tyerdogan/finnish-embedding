def main() -> None:
    """
    Entry point: mounts Google Drive (if in Colab), validates paths, builds
    config, and calls train().
    """

    # ── 1. Config ──────────────────────────────────────────────────────────────
    config = TrainingConfig()
    print("TrainingConfig")
    print("─" * 50)
    for k, v in config.__dict__.items():
        print(f"  {k:<30} = {v}")
    print("─" * 50)

    # ── 2. Google Drive mount ──────────────────────────────────────────────────
    try:
        from google.colab import drive  # type: ignore[import]
        mount_point = "/content/drive"
        if not Path(mount_point).exists():
            drive.mount(mount_point)
            print(f"Drive mounted at {mount_point}")
        else:
            print(f"Drive already mounted at {mount_point}")
    except ImportError:
        print("Not running in Colab — skipping Drive mount.")
        config.use_drive = False  # disable Drive mirror to avoid FileNotFoundError

    # ── 3. Path validation ─────────────────────────────────────────────────────
    root_dir = Path(__file__).resolve().parent.parent

    pt_path = root_dir / config.dataset_path
    if not pt_path.exists():
        raise FileNotFoundError(
            f"Pre-processed dataset not found: {pt_path}\n"
            "Run  python src/04_dataset.py  first to generate dataset.pt."
        )

    tok_dir = root_dir / config.tokenizer_dir
    if not tok_dir.exists():
        raise FileNotFoundError(
            f"Tokenizer directory not found: {tok_dir}\n"
            "Run  python src/03_tokenizer.py  first."
        )

    # ── 4. Train ───────────────────────────────────────────────────────────────
    loss_history = train(config)

    # ── 5. Summary ─────────────────────────────────────────────────────────────
    if loss_history:
        n_last        = min(100, len(loss_history))
        avg_last      = sum(loss_history[-n_last:]) / n_last
        final_loss    = loss_history[-1]
        final_ppl     = math.exp(min(final_loss, 20))
        initial_loss  = loss_history[0]

        print("\n── Final Summary " + "─" * 34)
        print(f"  Steps completed     : {len(loss_history):,}")
        print(f"  Initial loss        : {initial_loss:.4f}  "
              f"(random-init expected ≈ {math.log(50_000):.2f} = ln(50 000))")
        print(f"  Final loss          : {final_loss:.4f}")
        print(f"  Avg loss (last {n_last:>3}) : {avg_last:.4f}")
        print(f"  Final perplexity    : {final_ppl:.1f}")
        print("─" * 50)


if __name__ == "__main__":
    main()
